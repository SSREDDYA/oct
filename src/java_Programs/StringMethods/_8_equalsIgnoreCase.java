package java_Programs.StringMethods;

public class _8_equalsIgnoreCase {
	

	public static void main(String[] args)
	{

String str="SivaSankarReddy";
String str1="SivaSankarReddy";
String str2="ShivaShankarReddy";
String str3="SIVASANKARREDDY";

	System.out.println("str and str1 is same  := "+str.equalsIgnoreCase(str1));
	System.out.println("str and str3 is same  := "+str.equalsIgnoreCase(str3));
	System.out.println("str and str2 is same  := "+str.equalsIgnoreCase(str2));
	
	
	
}	
	
}
